
public class MultiplicationTable{
	public static void main(String...args){
		
	for(int count =1; count <= 10; count++){
	
		for ( int counter = 1; counter<= 10; counter++){
		System.out.printf("%d * %d = %d", count, counter, counter * count);
		System.out.println();
		}
		System.out.println();

	}
	
	}

}